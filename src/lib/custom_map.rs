use super::{Tile, TileType};

fn compile(tiles: Vec<Vec<&str>>) -> Vec<Vec<Tile>> {
    let o: Tile = Tile::new(TileType::Empty);
    let g: Tile = Tile::new(TileType::Grass);
    let x: Tile = Tile::new(TileType::Wall);

    let mut new_tiles: Vec<Vec<Tile>> = Vec::new();

    for i in 0..tiles.len(){
        let mut row: Vec<Tile> = Vec::new();
        for j in 0..tiles[0].len(){
            let tile = match tiles[i][j] {
                "拘勇" => x,
                "游릱" => o,
                "游릴" => g,
                _ => o
            };

            row.push(tile);
        }
        new_tiles.push(row);
    }

    return new_tiles;
}

